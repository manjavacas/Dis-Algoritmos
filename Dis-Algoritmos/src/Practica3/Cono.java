package Practica3;

public class Cono {

	private double radio;
	private double altura;

	public Cono(double radio, double altura) {
		this.radio = radio;
		this.altura = altura;
	}

	public double getRadio() {
		return radio;
	}

	public void setRadio(double radio) {
		this.radio = radio;
	}

	public double getAltura() {
		return altura;
	}

	public void setAltura(double altura) {
		this.altura = altura;
	}

}
